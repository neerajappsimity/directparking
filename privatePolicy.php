COLLECTION OF INFORMATION</h2>
<p>
We collect information you provide directly to us, such as when you create or modify your account,
request Transportation Services, contact support, or otherwise communicate with us. This information
may include: name, birth date, school name, school email, phone number, profile picture, Car
Information, Drivers License number, and other information you choose to provide.</p>
